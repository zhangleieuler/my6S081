uint64
sys_exit(void)
{
  int n;
  argint(0, &n);
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  argaddr(0, &p);
  return wait(p);
}

uint64
sys_sbrk(void)
{
  uint64 addr;
  int n;

  argint(0, &n);
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)                    //sleep system call 
{
  int n;                           //time of sleep
  uint ticks0;                     // ticks of current system

  argint(0, &n);                   //get the arguments,get the first args form user space store to n,
  acquire(&tickslock);             //get the lock to protect ticks
  ticks0 = ticks;                  //record the current ticks
  while(ticks - ticks0 < n){ 
    if(killed(myproc())){          //check wheter process is killed
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);      //ticks is the conditional variable
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  argint(0, &pid);
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}
